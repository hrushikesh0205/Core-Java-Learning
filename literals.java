public class literals {
    
    //Literal is a constant value assigned directly to a variable in Java
    public static void main(String[] args) {

        int age = 21;                 // Integer literal
        double price = 99.99;         // Floating-point literal
        char grade = 'A';             // Character literal
        String name = "Hrushikesh";   // String literal
        boolean isStudent = true;     // Boolean literal

        System.out.println("Age: " + age);
        System.out.println("Price: " + price);
        System.out.println("Grade: " + grade);
        System.out.println("Name: " + name);
        System.out.println("Is Student: " + isStudent);
    }
}

    

